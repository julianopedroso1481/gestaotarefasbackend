CREATE TABLE IF NOT EXISTS TAREFA (
      ID_TAREFA bigint generated BY DEFAULT AS identity,
      DATA_CRIACAO TIMESTAMP(6), 
      DESCRICAO VARCHAR(500), 
      STATUS VARCHAR(20), 
      TITULO VARCHAR(200) NOT null,
      ID_PROJETO bigint,
      PRIMARY KEY (ID_TAREFA)
);

CREATE TABLE IF NOT EXISTS PROJETO (
	ID_PROJETO bigint generated BY DEFAULT AS identity, 
	NOME VARCHAR(200) NOT null, 
PRIMARY KEY (ID_PROJETO));

ALTER TABLE if EXISTS TAREFA ADD CONSTRAINT ID_PROJETO FOREIGN KEY (ID_PROJETO) REFERENCES PROJETO;